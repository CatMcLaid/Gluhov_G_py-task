# Ввод размеров матрицы
n, m = map(int, input("Введите количество строк и столбцов: ").split())

# Ввод матрицы
matrix = []
print("Введите элементы матрицы:")
for i in range(n):
    row = list(map(float, input().split()))
    matrix.append(row)

# Нахождение среднего арифметического столбцов с четными номерами
# (2-й, 4-й, 6-й ... столбцы)
for j in range(1, m, 2):  # индексы 1,3,5... соответствуют четным номерам
    s = 0
    for i in range(n):
        s += matrix[i][j]
    
    avg = s / n
    print(f"Среднее арифметическое {j + 1}-го столбца: {avg}")
